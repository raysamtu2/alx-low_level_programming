#include <stdio.h>



/**

 * main - Entry point

 *

 * Return: Always 0 (Success)

 */

int main(void)

{

	char digits;



	for (digit = '0'; digit <= '9'; digits++)

	{

		putchar(digits);

	}

	for (digit = 'a'; digit <= 'f'; digits++)

	{

		putchar(digits);

	}

	putchar('\n');

	return (0);

}

